## 建立binary tree 記得左大右小，從根節點開始
## 建立binary tree 需要元素有 建立 刪除 搜尋
## binary tree 種類有 
## full binary tree 除了子葉以外都有兩個子節點
## complete binary tree 除了最深層的以外其他曾節點全滿
## balanced binary tree 每個節點的2個子節點深度不可差超過一
## perfect binary tree 全滿
## 每i層有最多2^(i-1個節點) 
## 一顆完美二元樹深度為節點數為2^k-1
## n個節點總共有logn +1 層
## 搜尋時間為 O(logn)
